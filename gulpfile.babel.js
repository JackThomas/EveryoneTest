//  ============================================================================================================
//  Task imports
//  ============================================================================================================

import gulp from "gulp";
import require from "require-dir";

// Grab Relevent Tasks
require("./gulp/tasks/", { recurse: true });


//  ============================================================================================================
//  Clean
//  ============================================================================================================
gulp.task("clean", ["clean:html", "clean:css", "clean:js"]);


//  ============================================================================================================
//  Default
//  ============================================================================================================
gulp.task("default", ["assemble", "sass", "symbols:web", "images:svg", "images:raster", "browserify", "fonts"]);


//  ============================================================================================================
//  Development
//  ============================================================================================================
gulp.task("dev", ["default"], function defaultTask() {
	gulp.start("watch");
});


//  ============================================================================================================
//  Watch tasks
//  ============================================================================================================

gulp.task("watch", ["browsersync"], function watch() {
	gulp.watch("src/**/*.hbs", ["watch:assemble"]);
	gulp.watch("src/scss/**/*.scss", ["watch:sass"]);
	gulp.watch("src/js/**/*.js", ["watch:browserify"]);
});
